<?php

namespace Templite\Cms\Concerns;

/**
 * Трейт для моделей, реализующих Exportable.
 *
 * Предоставляет дефолтную реализацию для сбора медиафайлов,
 * на которые ссылается сущность. Эти файлы будут включены
 * в ZIP-архив при экспорте.
 *
 * Модели могут переопределить getExportMediaFiles() для указания
 * конкретных файлов (например, изображения блока, файлы страницы).
 */
trait HasExportable
{
    /**
     * Медиафайлы, на которые ссылается сущность.
     *
     * Возвращает массив путей к файлам (относительно storage/app/public)
     * для включения в ZIP-архив при экспорте.
     *
     * @return string[]
     */
    public function getExportMediaFiles(): array
    {
        return [];
    }
}
